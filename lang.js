function changeLanguage(lang) {
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[lang] && translations[lang][key]) {
            element.innerHTML = translations[lang][key];
        }
    });

    if (lang === 'jp') {
        document.querySelector('.steam-button').innerHTML = '参加する';
        document.querySelector('.partner-button').innerHTML = 'パートナーになる';
    } else if (lang === 'en') {
        document.querySelector('.steam-button').innerHTML = 'Join';
        document.querySelector('.partner-button').innerHTML = 'Become a Partner';
    } else {
        document.querySelector('.steam-button').innerHTML = 'Перейти';
        document.querySelector('.partner-button').innerHTML = 'Стать Партнером';
    }
}

changeLanguage('en');
